
//Let's create a function to add a two number

//1.Function Defination Area (one time process)

function addTwoNumbers(n1,n2){  //camelCaseLetter //n1,n2 are formal Argument
    //Function body
    //Inside the function body I can write many Statement
    //Every function return something
    return n1 + n2;
    
}

//2.Function Calling/Invoking Area (many time process)

var result = addTwoNumbers(10,30);  //Actual Arguments
console.log(result);
